// Conditional Statement in JS

// Variable -> It is a type of container used to store data/values

// In Javascript we can make variable by three ways
/*
1) let -> They are changeable and cannot be Redeclared (Block Scope Variable)
2) const -> They are not changeable and cannot be Redeclared (Block Scope Variable)
3) var -> They are changeable and can be Redeclared (it is global scope Variable)
*/


const  pi = 3.14;
console.log(pi);

// Let 

let x = 'Name';
console.log(x);

x += " Singh"
console.log(x);

x = 10
console.log(x);

// Var

